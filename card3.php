        <p class="intro-text">
            Airbnb runs Superhost evaluations every three months, on the first day of January, April, July, and October.

When you log into your Airbnb dashboard, go to your Performance tab. Under Opportunities, you'll see exactly where you stand on each requirement. You can track your rating, response rate, cancellation rate, and number of stays in real time.

If you meet all four requirements by an evaluation date, you automatically become a Superhost. Airbnb notifies you at the end of the assessment period. The badge can take up to a week to appear on your listings.

You don't need to apply or do anything special. Once you hit the numbers, the status is awarded automatically.
